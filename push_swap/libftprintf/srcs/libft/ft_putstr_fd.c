#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int res;

	i = 0;
	while (s[i])
	{
		res = write(fd, &s[i], 1);
		i++;
		if (res == -1)
			return ;
	}
}
